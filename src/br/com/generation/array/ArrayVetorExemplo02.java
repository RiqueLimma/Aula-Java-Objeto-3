package br.com.generation.array;

public class ArrayVetorExemplo02 {

	public static void main(String[] args) {
		
		double minhaNota;
		double[] notas = new double [2];
		
		notas[0] = 9.6;
		minhaNota = notas[0];
		
		System.out.println("A variável contém: " + minhaNota);
		System.out.println("A variável contém: notas[0]: " + notas[0]);
	}

}
